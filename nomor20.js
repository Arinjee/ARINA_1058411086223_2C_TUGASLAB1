// 19. Konversi Boolean ke String dan Number
let bool = true;

// Konversi Boolean ke String
let boolStr = String(bool);
console.log(boolStr); // Output: "true"
console.log(typeof boolStr); // Output: "string"

// Konversi Boolean ke Number
let boolNum = Number(bool);
console.log(boolNum); // Output: 1
console.log(typeof boolNum); // Output: "number"
